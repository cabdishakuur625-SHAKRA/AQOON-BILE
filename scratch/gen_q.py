    correct_key = ""
    for idx, (k, v) in enumerate(items):
        new_key = chr(97 + idx) # a, b, c, d
        new_opts[new_key] = v
        if k == "a":
            correct_key = new_key
            
    return {
        "id": f"Tar_Ch4_Q{q_id:02d}",
        "question": question,
        "options": new_opts,
        "correctAnswer": correct_key,
        "difficultyLevel": diff,
        "subjectId": "tarbiyo",
        "chapterId": "tarbiyo_ch4"
    }

